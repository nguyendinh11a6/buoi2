// Khai báo biến
let a = 15;
let b = 4;

// Thực hiện các phép toán
let tong = a + b;
let hieu = a - b;
let tich = a * b;
let thuong = a / b;
let duSo = a % b;
let luyThua = a ** b;

// In kết quả ra console
console.log("Tổng: " + tong);         // 19
console.log("Hiệu: " + hieu);         // 11
console.log("Tích: " + tich);         // 60
console.log("Thương: " + thuong);     // 3.75
console.log("Dư số: " + duSo);        // 3
console.log("Lũy thừa: " + luyThua);  // 50625 (15^4)